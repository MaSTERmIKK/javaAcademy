public class variabili {
    public static void main(String[] args) {
        int provaNumero = 12; // dichiarazione e inizializzazione variabile intera
        String provaTesto = "Ciao mondo"; // dichiarazione e inizializzazione variabile stringa
        boolean provaBool; // dichiarazione variabile booleana
        provaBool = false; // assegna false alla variabile booleana
        provaBool = true; // assegna true alla variabile booleana
        System.out.println(provaTesto); // stampa stringa
        System.out.println(provaNumero); // stampa intero
        System.out.println(provaBool); // stampa booleano

    }
}
